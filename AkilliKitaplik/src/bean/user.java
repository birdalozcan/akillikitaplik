package bean;

public class user {

	private String uye_id;
	private String uye_adi = "";
	private String uye_soyadi = "";
	private String eposta = "";
	private String sifre;

	public String getUye_id() {
		return uye_id;
	}

	public void setUye_id(String uye_id) {
		this.uye_id = uye_id;
	}

	public String getUye_adi() {
		return uye_adi;
	}

	public void setUye_adi(String uye_adi) {
		this.uye_adi = uye_adi;
	}

	public String getUye_soyadi() {
		return uye_soyadi;
	}

	public void setUye_soyadi(String uye_soyadi) {
		this.uye_soyadi = uye_soyadi;
	}

	public String getEposta() {
		return eposta;
	}

	public void setEposta(String eposta) {
		this.eposta = eposta;
	}

	public String getSifre() {
		return sifre;
	}

	public void setSifre(String sifre) {
		this.sifre = sifre;
	}

}
